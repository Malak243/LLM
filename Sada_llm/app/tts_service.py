import edge_tts
import uuid
import os
from mishkal.tashkeel import TashkeelClass

# ============================================================
# مشكل عربي
# ============================================================

vocalizer = TashkeelClass()

# ============================================================
# تنظيف النص
# ============================================================

def normalize_text(text: str):

    text = text.strip()

    # إزالة الإيموجي
    text = text.replace("🎉", "")
    text = text.replace("🤗", "")
    text = text.replace("🌟", "")
    text = text.replace("❤️", "")

    # إزالة المسافات الزائدة
    text = " ".join(text.split())

    return text

# ============================================================
# إصلاح الكلمات الشائعة
# ============================================================

def fix_common_words(text: str):

    fixes = {

        "بطل": "بَطِّل",

        "كويس": "كُوَيِّس",
        "شاطر": "شاطِر",
        "جامد": "جامِد",
        "حلو": "حِلو"

    }

    for wrong, correct in fixes.items():

        text = text.replace(
            wrong,
            correct
        )

    return text

# ============================================================
# تشكيل عربي تلقائي
# ============================================================

def add_tashkeel(text: str):

    try:

        tashkeel_text = vocalizer.tashkeel(text)

        return tashkeel_text

    except Exception:

        return text

# ============================================================
# تحويل النص لصوت
# ============================================================

async def text_to_speech(text: str):

    # ========================================================
    # تنظيف النص
    # ========================================================

    text = normalize_text(text)

    # ========================================================
    # إصلاح الكلمات الشائعة
    # ========================================================

    text = fix_common_words(text)

    # ========================================================
    # تشكيل تلقائي
    # ========================================================

    text = add_tashkeel(text)

    # ========================================================
    # إنشاء فولدر الصوت
    # ========================================================

    os.makedirs("audio", exist_ok=True)

    # ========================================================
    # اسم الملف
    # ========================================================

    file_name = (
        f"audio_{uuid.uuid4()}.mp3"
    )

    file_path = os.path.join(
        "audio",
        file_name
    )

    # ========================================================
    # إعدادات الصوت
    # ========================================================

    communicate = edge_tts.Communicate(
        text=text,
        voice="ar-EG-ShakirNeural",
        rate="-25%"
    )

    # ========================================================
    # حفظ الصوت
    # ========================================================

    await communicate.save(file_path)

    # ========================================================
    # إرجاع المسار
    # ========================================================

    return file_path